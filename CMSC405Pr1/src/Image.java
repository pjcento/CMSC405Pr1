    // Method similar to getImage given in P1 template, modified to use switch
    // statement in place of if/else to support 4 color options
    public BufferedImage createShape(int[][] shape)
    {
	BufferedImage image = new BufferedImage(SIZEX, SIZEY, BufferedImage.TYPE_INT_RGB);

	for (int x = 0; x < SIZEX; x++) {
	    for (int y = 0; y < SIZEY; y++) {
		int color = shape[x][y];
		switch (color) {
		case 1:
		    color = Color.RED.getRGB();
		    break;
		case 2:
		    color = Color.GREEN.getRGB();
		    break;
		case 3:
		    color = Color.BLUE.getRGB();
		    break;
		default:
		    color = Color.BLACK.getRGB();
		    break;
		}
		image.setRGB(x, y, color);
	    } // y
	} // x
	return image;
    }// createShape
